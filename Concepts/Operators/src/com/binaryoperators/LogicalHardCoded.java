package com.binaryoperators;

public class LogicalHardCoded {
public static void main(String[] args) {
	boolean a=true;
	boolean b=false;
	System.out.println("AND Operation Result: "+(a&&b));
	System.out.println("OR Operation Result: "+(a||b));
	System.out.println("AND Operation Result: "+(true&&true));
	System.out.println("OR Operation Result: "+(false||true));

}
}
